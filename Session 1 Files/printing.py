var1 = 3
var2 = 3.14
var3 = False
var4 = 'String1'
var5 = "String2"
var6 = [var1, var2, var3, var4, var5]

# Printing
print(var1)

# Comma printing
print(var2, var3, var4, var5)

# Array printing
print(var6)

# F-strings
name = 'Vincent'
print(f'Hello, my name is {name}')

value = 3.14159
print(f'Pi is about {value:.2f}')

# You can use f-strings like this, too
print(f'1 + 1 = {1 + 1}')